def add(a, b):
    """
    add a and b
    """
    return a+b


def minus(a, b):
    """
    subtract b from a
    """
    return a-b


def multiply(a, b):
    """
    multiply a and b
    """
    return a*b


def divide(a, b):
    """
    divide a by b
    """
    return a/b